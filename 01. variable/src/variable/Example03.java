package variable;

public class Example03 {

	public static void main(String[] args) {

		// 실수 타입
		// 소수점을 표기할 수 있는 타입
		// float, double
		
		
		// 1. float
		// 4byte > 32bit > 그러나 일부는 실수부(소수점 이하)로 쓰임
		float f;
		f = 2.5f;
		// long처럼 뒤에 단위처럼 f를 함께 써 줘야 함
		System.out.println(f);
		
		//2. double
		double d;
		d = 5;
		// 단위 필요 없음!
		System.out.println(d);
		
	}

}
